// SIMULATION — drives the REAL Studio engine over live data (no duplicate logic).
// Hits /api/pro-personnel/trade-studio/generate per team/block, exactly what ships.
// Requires the dev server running on :3000.
import { writeFileSync } from "node:fs";

const BASE = "http://localhost:3000";
const norm = (s) => (s || "").toLowerCase().replace(/\b(jr|sr|ii|iii|iv|v)\b/g, "").replace(/[^a-z0-9]/g, "");
const r0 = (n) => Math.round(n);
const r2 = (n) => Math.round(n * 100) / 100;

// Curated test blocks (carried from the prior sim) — names resolved to keys per team.
const BLOCKS = {
  1: { A: ["Daniel Jones"], B: ["Kyle Monangai", "Jacory Croskey-Merritt"], C: ["Parker Washington", "Jalen Coker"] },
  2: { A: ["Jordan Mason", "Kendre Miller"], B: ["Jauan Jennings", "Rashid Shaheed"], C: ["Jordan James", "Rashid Shaheed"], D: ["Jordan Mason", "Jaylin Lane"], E: ["Marcus Mariota", "Will Levis"], F: ["Rashid Shaheed", "Jordan Mason", "Jauan Jennings"] },
  3: { A: ["De'Von Achane", "Tee Higgins"], B: ["Josh Jacobs", "Mike Evans"], C: ["Travis Etienne"] },
  4: { A: ["Anthony Richardson", "Jameson Williams"] },
  5: { A: ["Emeka Egbuka", "Michael Penix"] },
  6: { A: ["Kyler Murray", "Bryce Young"], B: ["Derrick Henry", "Rhamondre Stevenson"], C: ["Terry McLaurin", "DK Metcalf"], D: ["Davante Adams", "Derrick Henry"], E: ["Lamar Jackson", "Tyler Warren"], F: ["Stefon Diggs", "Rachaad White"] },
  7: { A: ["Zach Charbonnet", "Braelon Allen"], B: ["Christian Watson", "Alec Pierce"], C: ["Ricky Pearsall", "Isaac Guerendo"] },
  8: { A: ["Chris Olave", "Rashee Rice"], B: ["Brock Purdy"], C: ["Nico Collins", "Marvin Harrison"] },
  9: { A: ["Zay Flowers", "DeVonta Smith"], B: ["Courtland Sutton", "Tre Tucker"], C: ["James Cook"], D: ["Geno Smith", "Najee Harris"] },
  10: { A: ["Aaron Jones", "Chris Godwin"], B: ["Trevor Lawrence", "Tucker Kraft"], C: ["Tyler Shough", "Mark Andrews"] },
  11: { A: ["Ladd McConkey", "Wan'Dale Robinson"], B: ["Ladd McConkey", "Wan'Dale Robinson", "Michael Wilson"], C: ["Aaron Rodgers", "Davis Mills"], D: ["C.J. Stroud"], E: ["Trey McBride"], F: ["Javonte Williams"] },
  12: { A: ["Dak Prescott"], B: ["Josh Downs", "Jayden Reed"] },
};

const tg = await (await fetch(`${BASE}/api/pro-personnel/targets?teamId=1`)).json();
const rosters = tg.rosters || {};
const names = {};
const byName = {};
for (const [tid, assets] of Object.entries(rosters)) {
  names[tid] = assets[0]?.teamName || `Team ${tid}`;
  const m = {};
  for (const a of assets) m[norm(a.name)] = a;
  byName[tid] = m;
}

async function generate(teamId, shopKeys) {
  const res = await fetch(`${BASE}/api/pro-personnel/trade-studio/generate`, {
    method: "POST",
    headers: { "content-type": "application/json" },
    body: JSON.stringify({ team_id: teamId, shop_list_keys: shopKeys }),
  });
  return res.json();
}

let out = "";
const warn = [];
for (let t = 1; t <= 12; t++) {
  const tid = String(t);
  out += `\n## Team ${t} · ${names[tid]}\n`;
  for (const [label, sendNames] of Object.entries(BLOCKS[t] || {})) {
    const assets = sendNames.map((n) => { const a = byName[tid]?.[norm(n)]; if (!a) warn.push(`T${t} ${label}: ${n}`); return a; }).filter(Boolean);
    if (!assets.length) continue;
    const shopKeys = assets.map((a) => a.key);
    const sendLabel = assets.map((a) => `${a.name} (${r0(a.value)})`).join(", ");
    const resp = await generate(tid, shopKeys);
    const offers = resp.offers || [];
    const intent = offers[0]?.blockIntent ?? "—";
    out += `\n**Block ${label}** — we send: ${sendLabel}  ·  _intent: **${intent}**_ · ${offers.length} offers\n\n`;
    out += `| ID | Partner | We get (cfc each) | Recv | Ratio | Grade | 🗒 Your feedback |\n|----|---|---|:--:|:--:|:--:|---|\n`;
    if (!offers.length) {
      out += `| — | _no partner-viable offers in band_ | — | — | — | — | |\n`;
    } else {
      offers.forEach((o, i) => {
        const gets = (o.receive || []).map((a) => `${a.name} (${r0(a.value)})`).join(" + ");
        const ratio = o.valueGap?.ratio ?? (o.receiveValue / o.sendValue);
        out += `| T${tid}-${label}${i + 1} | ${o.partnerTeamName} | ${gets} | ${r0(o.receiveValue)} | ${r2(ratio)} | ${o.gradeLabel} | |\n`;
      });
    }
  }
  out += `\n**Overall notes (Team ${t}):**\n`;
}

const header = `# Trade Studio — LIVE SIMULATION (real engine)
_Generated by driving the actual /api/pro-personnel/trade-studio/generate endpoint — what you see is exactly what ships._

## What's new in this run
- **SSOT alignment:** tiers = canonical impact/scrub sets (top-20 QB/RB, top-40 PC; scrub floor PC 80), starter slots = STARTERS (QB2/RB2/PC4), age = age.ts (exp<=3 / position bands), need = canonical levels. No arbitrary CFC floors.
- **Block intent** (shown per block): the lean inferred from the block — consolidate / upgrade_one / teardown / retool / cloudy — now floats matching offers to the top.
- **Studio lens (#5):** offers that work for the OTHER team surface even when they don't fit us; our-storyline fit is a ranking signal, not a filter.

## How to give feedback
Type in the **🗒 Your feedback** cell on any row, or the **Overall notes** line per team. Reference by ID.

> Ratio = receive cfc ÷ our send team-value (partner-ahead sits ~0.80–1.05).

`;
writeFileSync("TRADE_STUDIO_SIM_LIVE.md", header + out + "\n");
console.log("Wrote TRADE_STUDIO_SIM_LIVE.md");
if (warn.length) console.log("WARN unresolved names:", warn);
