-- Draft pipeline repair script (review-first, rerunnable)
-- Safety goals:
-- 1) Keep 2019 in canonical storage but flag as startup/non-rookie for historian query surfaces.
-- 2) Backfill only high-confidence Sleeper player mappings (no fuzzy/guess mapping).
-- 3) Rebuild ff_master_draft_picks from canonical actual-result sources.

BEGIN;

-- 0) Live schema inspection (run first in same script output)
SELECT table_schema, table_name, column_name, data_type
FROM information_schema.columns
WHERE (table_schema, table_name) IN (
  ('public', 'ff_master_draft_picks'),
  ('public', 'slp_mirror_draft_results'),
  ('public', 'ff_source_player_map'),
  ('public', 'ff_source_franchise_map'),
  ('llm', 'draft_picks'),
  ('llm', 'players')
)
ORDER BY table_schema, table_name, ordinal_position;

-- 1) Season classification: keep 2019 rows, but mark as startup (non-rookie)
CREATE TABLE IF NOT EXISTS public.ff_draft_season_flags (
  draft_year INTEGER PRIMARY KEY,
  is_rookie_draft BOOLEAN NOT NULL,
  classification TEXT NOT NULL,
  notes TEXT NULL,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

INSERT INTO public.ff_draft_season_flags (
  draft_year,
  is_rookie_draft,
  classification,
  notes,
  updated_at
)
VALUES (
  2019,
  FALSE,
  'startup',
  'Startup draft season; excluded from rookie-draft historian analysis.',
  NOW()
)
ON CONFLICT (draft_year)
DO UPDATE SET
  is_rookie_draft = EXCLUDED.is_rookie_draft,
  classification = EXCLUDED.classification,
  notes = EXCLUDED.notes,
  updated_at = NOW();

-- Optional query surface for rookie-only historian SQL without deleting canonical history.
CREATE OR REPLACE VIEW llm.draft_picks_rookie_only AS
SELECT dp.*
FROM llm.draft_picks dp
LEFT JOIN public.ff_draft_season_flags sf
  ON sf.draft_year = dp.season_year
WHERE COALESCE(sf.is_rookie_draft, TRUE);

-- 2) High-confidence Sleeper player mapping backfill
-- Rule: map only unresolved Sleeper source_player_id values with an exact normalized
-- full-name match to exactly one canonical llm.players row.

CREATE TABLE IF NOT EXISTS public.ff_draft_player_mapping_backfill_log (
  backfill_log_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  source_platform TEXT NOT NULL,
  source_player_id TEXT NOT NULL,
  candidate_player_name TEXT NULL,
  matched_player_id TEXT NULL,
  matched_player_name TEXT NULL,
  confidence TEXT NOT NULL,
  decision TEXT NOT NULL,
  decided_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (source_platform, source_player_id, decision)
);

WITH unresolved AS (
  SELECT DISTINCT
    sdr.source_player_id,
    COALESCE(
      NULLIF(BTRIM(sdr.metadata_json ->> 'full_name'), ''),
      NULLIF(BTRIM(CONCAT_WS(' ', sdr.metadata_json ->> 'first_name', sdr.metadata_json ->> 'last_name')), ''),
      NULLIF(BTRIM(sdr.metadata_json ->> 'player_name'), '')
    ) AS drafted_name
  FROM public.slp_mirror_draft_results sdr
  LEFT JOIN public.ff_source_player_map spm
    ON LOWER(spm.source_platform) = 'sleeper'
   AND spm.source_player_id = sdr.source_player_id
  WHERE sdr.source_player_id IS NOT NULL
    AND spm.player_id IS NULL
),
unresolved_norm AS (
  SELECT
    source_player_id,
    drafted_name,
    LOWER(REGEXP_REPLACE(COALESCE(drafted_name, ''), '[^a-z0-9]+', ' ', 'g')) AS drafted_name_norm
  FROM unresolved
),
canonical_norm AS (
  SELECT
    p.player_id,
    p.player_name,
    LOWER(REGEXP_REPLACE(COALESCE(p.player_name, ''), '[^a-z0-9]+', ' ', 'g')) AS player_name_norm
  FROM llm.players p
  WHERE p.player_name IS NOT NULL
),
joined AS (
  SELECT
    u.source_player_id,
    u.drafted_name,
    u.drafted_name_norm,
    c.player_id,
    c.player_name,
    COUNT(*) OVER (PARTITION BY u.source_player_id) AS candidate_count_for_source
  FROM unresolved_norm u
  JOIN canonical_norm c
    ON c.player_name_norm = u.drafted_name_norm
  -- Require at least one space character and at least 5 normalized characters to avoid
  -- high-risk matches on short/ambiguous aliases (e.g., initials, short nicknames).
  -- The 5-character floor prevents very short tokens (\"dj\", \"aj\", etc.) from auto-mapping.
  WHERE LENGTH(REPLACE(u.drafted_name_norm, ' ', '')) >= 5
    AND POSITION(' ' IN u.drafted_name_norm) > 0
),
high_confidence AS (
  SELECT
    source_player_id,
    drafted_name,
    player_id,
    player_name
  FROM joined
  WHERE candidate_count_for_source = 1
),
inserted_map AS (
  INSERT INTO public.ff_source_player_map (
    source_platform,
    source_player_id,
    player_id
  )
  SELECT
    'sleeper' AS source_platform,
    hc.source_player_id,
    hc.player_id
  FROM high_confidence hc
  WHERE NOT EXISTS (
    SELECT 1
    FROM public.ff_source_player_map spm
    WHERE LOWER(spm.source_platform) = 'sleeper'
      AND spm.source_player_id = hc.source_player_id
  )
  RETURNING source_player_id, player_id
)
INSERT INTO public.ff_draft_player_mapping_backfill_log (
  source_platform,
  source_player_id,
  candidate_player_name,
  matched_player_id,
  matched_player_name,
  confidence,
  decision
)
SELECT
  'sleeper' AS source_platform,
  hc.source_player_id,
  hc.drafted_name AS candidate_player_name,
  hc.player_id AS matched_player_id,
  hc.player_name AS matched_player_name,
  'high' AS confidence,
  CASE WHEN im.source_player_id IS NOT NULL THEN 'inserted' ELSE 'already_mapped' END AS decision
FROM high_confidence hc
LEFT JOIN inserted_map im
  ON im.source_player_id = hc.source_player_id;

-- Log low-confidence unresolved rows for manual review; do not auto-map.
WITH unresolved AS (
  SELECT DISTINCT
    sdr.source_player_id,
    COALESCE(
      NULLIF(BTRIM(sdr.metadata_json ->> 'full_name'), ''),
      NULLIF(BTRIM(CONCAT_WS(' ', sdr.metadata_json ->> 'first_name', sdr.metadata_json ->> 'last_name')), ''),
      NULLIF(BTRIM(sdr.metadata_json ->> 'player_name'), '')
    ) AS drafted_name
  FROM public.slp_mirror_draft_results sdr
  LEFT JOIN public.ff_source_player_map spm
    ON LOWER(spm.source_platform) = 'sleeper'
   AND spm.source_player_id = sdr.source_player_id
  WHERE sdr.source_player_id IS NOT NULL
    AND spm.player_id IS NULL
)
INSERT INTO public.ff_draft_player_mapping_backfill_log (
  source_platform,
  source_player_id,
  candidate_player_name,
  matched_player_id,
  matched_player_name,
  confidence,
  decision
)
SELECT
  'sleeper' AS source_platform,
  u.source_player_id,
  u.drafted_name,
  NULL,
  NULL,
  'low' AS confidence,
  'manual_review_required' AS decision
FROM unresolved u
ON CONFLICT (source_platform, source_player_id, decision)
DO NOTHING;

-- 3) Rebuild canonical actual draft picks with existing function
-- This preserves Flea/MFL actual rows and rebuilds Sleeper rows from mirror + maps,
-- including any high-confidence Sleeper mappings inserted above in this same script.
SELECT public.ff_rebuild_master_draft_picks_actual_results();

-- Immediate post-rebuild snapshot for operator verification.
SELECT
  draft_year,
  COALESCE(source_platform, 'unknown') AS source_platform,
  COUNT(*) AS rebuilt_rows
FROM public.ff_master_draft_picks
GROUP BY draft_year, COALESCE(source_platform, 'unknown')
ORDER BY draft_year, source_platform;

COMMIT;
