-- 2) Rebuild helper (actual results only)
--    Keeps existing Flea + MFL actual rows from ff_master_draft_picks,
--    replaces Sleeper rows from slp_mirror_draft_results + canonical maps.
CREATE OR REPLACE FUNCTION public.ff_rebuild_master_draft_picks_actual_results()
RETURNS VOID
LANGUAGE plpgsql
AS $$
DECLARE
  missing_columns TEXT;
BEGIN
  WITH required AS (
    SELECT *
    FROM (VALUES
      ('ff_master_draft_picks', 'draft_year'),
      ('ff_master_draft_picks', 'round'),
      ('ff_master_draft_picks', 'pick_number'),
      ('ff_master_draft_picks', 'selected_by_franchise_id'),
      ('ff_master_draft_picks', 'selected_player_id'),
      ('ff_master_draft_picks', 'original_franchise_id'),
      ('ff_master_draft_picks', 'current_franchise_id'),
      ('ff_master_draft_picks', 'source_platform'),
      ('ff_source_franchise_map', 'source_platform'),
      ('ff_source_franchise_map', 'source_league_id'),
      ('ff_source_franchise_map', 'source_franchise_id'),
      ('ff_source_franchise_map', 'franchise_id'),
      ('ff_source_player_map', 'source_platform'),
      ('ff_source_player_map', 'source_player_id'),
      ('ff_source_player_map', 'player_id')
    ) AS t(table_name, column_name)
  ),
  found AS (
    SELECT table_name, column_name
    FROM information_schema.columns
    WHERE table_schema = 'public'
      AND table_name IN ('ff_master_draft_picks', 'ff_source_franchise_map', 'ff_source_player_map')
  )
  SELECT string_agg(required.table_name || '.' || required.column_name, ', ')
  INTO missing_columns
  FROM required
  LEFT JOIN found
    ON found.table_name = required.table_name
   AND found.column_name = required.column_name
  WHERE found.column_name IS NULL;

  IF missing_columns IS NOT NULL THEN
    RAISE EXCEPTION 'Cannot rebuild ff_master_draft_picks. Missing required columns: %', missing_columns;
  END IF;

  CREATE TEMP TABLE tmp_ff_master_draft_picks_actual AS
  WITH flea_mfl_existing AS (
    SELECT
      draft_year,
      round,
      pick_number,
      selected_by_franchise_id,
      selected_player_id,
      selected_by_franchise_id AS original_franchise_id,
      selected_by_franchise_id AS current_franchise_id,
      source_platform
    FROM public.ff_master_draft_picks
    WHERE LOWER(source_platform) IN ('flea', 'fleaflicker', 'mfl')
  ),
  sleeper_actual AS (
    SELECT
      sdr.season_year AS draft_year,
      sdr.round,
      sdr.pick_number,
      sfm.franchise_id AS selected_by_franchise_id,
      spm.player_id AS selected_player_id,
      sfm.franchise_id AS original_franchise_id,
      sfm.franchise_id AS current_franchise_id,
      'sleeper'::text AS source_platform
    FROM public.slp_mirror_draft_results sdr
    JOIN public.ff_source_franchise_map sfm
      ON LOWER(sfm.source_platform) = 'sleeper'
     AND sfm.source_league_id = sdr.source_league_id
     AND sfm.source_franchise_id = sdr.roster_id
    JOIN public.ff_source_player_map spm
      ON LOWER(spm.source_platform) = 'sleeper'
     AND spm.source_player_id = sdr.source_player_id
    WHERE sdr.pick_number IS NOT NULL
  )
  SELECT * FROM flea_mfl_existing
  UNION ALL
  SELECT * FROM sleeper_actual;

  TRUNCATE TABLE public.ff_master_draft_picks;

  INSERT INTO public.ff_master_draft_picks (
    draft_year,
    round,
    pick_number,
    selected_by_franchise_id,
    selected_player_id,
    original_franchise_id,
    current_franchise_id,
    source_platform
  )
  SELECT
    draft_year,
    round,
    pick_number,
    selected_by_franchise_id,
    selected_player_id,
    original_franchise_id,
    current_franchise_id,
    source_platform
  FROM tmp_ff_master_draft_picks_actual;
END;
$$;

-- 3) Validation queries
-- 3a. Sleeper mirror counts by season
SELECT season_year, COUNT(*) AS sleeper_pick_count
FROM public.slp_mirror_draft_results
GROUP BY season_year
ORDER BY season_year;

-- 3b. Master draft picks by platform and year
SELECT source_platform, draft_year, COUNT(*) AS pick_count
FROM public.ff_master_draft_picks
GROUP BY source_platform, draft_year
ORDER BY draft_year, source_platform;

-- 3c. Sleeper duplicate check (should return 0 rows)
SELECT draft_id, pick_number, COUNT(*) AS dup_count
FROM public.slp_mirror_draft_results
GROUP BY draft_id, pick_number
HAVING COUNT(*) > 1;

-- 3d. Expected Sleeper years present in mirror
SELECT season_year, COUNT(*) AS pick_count
FROM public.slp_mirror_draft_results
WHERE season_year IN (2024, 2025)
GROUP BY season_year
ORDER BY season_year;
